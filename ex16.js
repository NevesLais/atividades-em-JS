let temp = 35;

if (temp < 20) {
  console.log("Frio");
} else if (temp >= 20 && temp <= 29) {
  console.log("Ameno");
} else {
  console.log("Quente");
}


/*Variável temp: Aqui definimos a variável temp com o valor 35, que representa a temperatura.

Estrutura if-else:

Primeira verificação: Se a temperatura for menor que 20°C, o programa imprime "Frio".

Segunda verificação: Se a temperatura for de 20°C a 29°C, o programa imprime "Ameno".

Caso contrário: Se a temperatura for 30°C ou mais, o programa imprime "Quente".*/