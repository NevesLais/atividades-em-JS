let dia = 3; // Pode mudar o valor para testar outros dias

switch (dia) {
  case 1:
    console.log("Domingo");
    break;
  case 2:
    console.log("Segunda-feira");
    break;
  case 3:
    console.log("Terça-feira");
    break;
  case 4:
    console.log("Quarta-feira");
    break;
  case 5:
    console.log("Quinta-feira");
    break;
  case 6:
    console.log("Sexta-feira");
    break;
  case 7:
    console.log("Sábado");
    break;
  default:
    console.log("Dia inválido"); // Caso o número não seja de 1 a 7
}

//O switch verifica o valor da variável dia.

//Ele compara esse valor com os case definidos.

//Quando o valor de dia corresponder a um dos case, o código dentro desse case será executado.

//O break é usado para garantir que a execução do código pare naquele ponto e não continue nos outros casos.
 
